#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	while (*lst != NULL)
	{
		tmp = *lst;
		*lst = tmp -> next;
		del(tmp -> content);
		free(tmp);
		tmp = NULL;
	}
	return ;
}
